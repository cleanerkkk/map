package com.example.map.model;

public class CityInfo {
    private String cityName;
    private String culturalIntroduction;
    private String history;
    private String agriculture;
    private String industry;
    private String naturalFeatures;
    private String climate;
    private String topography;
    private String hydrology;


    // Getters for each field
    public String getCulturalIntroduction() { return culturalIntroduction; }
    public String getHistory() { return history; }
    public String getAgriculture() { return agriculture; }
    public String getIndustry() { return industry; }
    public String getNaturalFeatures() { return naturalFeatures; }
    public String getClimate() { return climate; }
    public String getTopography() { return topography; }
    public String getHydrology() { return hydrology; }
    public String getCityName() { return cityName; }
}